@responses.activate
def test_send_message_post_endpoint_returns_single_sent_message(client, register, json_body):
    """send_message is a POST that returns one SentMessage, not a Response."""
    register("POST", "/conversations/messages/send", body=json_body(
        data=[{
            "id": "conv1",
            "message_id": "msg1",
            "message_reference_id": "ref1",
            "status": "queued",
        }],
        meta={"current_page": 1, "last_page": 1},
    ))

    result = client.send_message(phone="+1234", text="hello")

    assert isinstance(result, SentMessage)
    assert result.message_id == "msg1"
    assert responses.calls[0].request.method == "POST"
    body = ujson.loads(responses.calls[0].request.body)
    assert body == {"phone": "+1234", "text": "hello"}


