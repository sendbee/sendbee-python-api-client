@responses.activate
def test_subscribe_contact_default_parameters_block_automation_and_notifications(
    client, register, json_body
):
    """subscribe_contact's bind-site defaults set block_automation=block_notifications=True."""
    register("POST", "/contacts/subscribe", body=json_body(
        data=[{
            "id": "c1", "name": "Alice", "phone": "+1234",
            "status": "subscribed", "tags": [], "contact_fields": [], "notes": [],
        }],
        meta={"current_page": 1, "last_page": 1},
    ))

    client.subscribe_contact(phone="+1234", name="Alice")

    body = ujson.loads(responses.calls[0].request.body)
    assert body["block_automation"] is True
    assert body["block_notifications"] is True
    assert body["phone"] == "+1234"
    assert body["name"] == "Alice"
